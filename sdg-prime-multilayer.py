# Backfill workers
# ══════════════════════════════════════════════════════════════════════════════

def _apm_day_worker(es, day_dt, trace_count, anomaly_chance,
                    bulk_size, pb_threads, pb_queue, progress_q, tz):
    metrics_counter = [0]

    def _gen():
        for ts in timestamps_for_day(day_dt, trace_count, tz):
            yield from _make_apm_actions(ts, anomaly_chance, metrics_counter)

    try:
        for ok, info in parallel_bulk(
            es, _gen(),
            thread_count=pb_threads, chunk_size=bulk_size,
            queue_size=pb_queue, raise_on_error=False,
            raise_on_exception=False, request_timeout=120,
        ):
            progress_q.put(("APM", "OK" if ok else f"ERR:{info}"))
    except Exception as e:
        progress_q.put(("APM", f"ERR:{day_dt}:{e}"))


def _edge_day_worker(es, day_dt, event_count, bot_attack_chance,
                     bulk_size, pb_threads, pb_queue, progress_q, tz):
    def _gen():
        for ts in timestamps_for_day(day_dt, event_count, tz):
            is_attack = random.random() < bot_attack_chance
            yield from _make_edge_actions(ts, is_attack)

    try:
        for ok, info in parallel_bulk(
            es, _gen(),
            thread_count=pb_threads, chunk_size=bulk_size,
            queue_size=pb_queue, raise_on_error=False,
            raise_on_exception=False, request_timeout=120,
        ):
            progress_q.put(("EDGE", "OK" if ok else f"ERR:{info}"))
    except Exception as e:
        progress_q.put(("EDGE", f"ERR:{day_dt}:{e}"))


# ══════════════════════════════════════════════════════════════════════════════
# Backfill orchestration
# ══════════════════════════════════════════════════════════════════════════════

def run_backfill(host, user, password, verify_ssl,
                 days, apm_tpd, edge_epd, anomaly_chance, bot_attack_chance, tz,
                 workers, bulk_size_apm, bulk_size_edge, pb_threads, pb_queue):
    """
    Generate historical data for all three streams in parallel.
    APM and EDGE work-queues run concurrently using worker thread pools.
    Returns (backfill_days, apm_tpd, today_apm_written) for use by live generator.
    """
    ssl_opts = {"verify_certs": verify_ssl, "ssl_show_warn": False}
    if not verify_ssl:
        ssl_opts["ssl_assert_fingerprint"] = None
    es = Elasticsearch(host, basic_auth=(user, password), **ssl_opts)

    today     = datetime.now(tz).date()
    start_day = today - timedelta(days=days)
    tz_name   = tz_str(tz)

    # Build per-day schedule (excludes today — today is handled by live mode)
    schedule = []
    for d in range(days):
        day = start_day + timedelta(days=d)
        schedule.append({
            "day":        day,
            "apm_count":  max(1, round(apm_tpd  * _day_factor(day))),
            "edge_count": max(1, round(edge_epd * _day_factor(day))),
        })

    est_docs_per_trace = 10
    total_apm_traces   = sum(s["apm_count"]  for s in schedule)
    total_edge_events  = sum(s["edge_count"] for s in schedule)
    total_docs         = total_apm_traces * est_docs_per_trace + total_edge_events * 2

    print(f"\n{'='*70}")
    print(f"  SDG-Prime Multilayer — Historical Backfill")
    print(f"{'='*70}")
    print(f"  Streams:        traces-apm-default")
    print(f"                  logs-akamai.siem-mortgage")
    print(f"                  logs-aws.waf-mortgage")
    print(f"  Days:           {days}  ({start_day} → {today - timedelta(days=1)})")
    print(f"  APM (weekday):  {apm_tpd:,} traces/day  (~{apm_tpd*est_docs_per_trace:,} docs)")
    print(f"  Edge (weekday): {edge_epd:,} events/day  ({edge_epd*2:,} docs: Akamai + WAF)")
    print(f"  Anomaly chance: {anomaly_chance:.0%}  geo-anomalous APM traces")
    print(f"  Bot/attack:     {bot_attack_chance:.0%}  of edge events are attack sessions")
    print(f"  Timezone:       {tz_name}")
    print(f"  ~Total docs:    {total_docs:,}")
    print(f"  CPU cores:      {_CPU_CORES} → workers={workers}, pb_threads={pb_threads}")
    print()

    if "utc" in tz_name.lower() or tz_name in ("UTC", "Etc/UTC", "GMT"):
        print("  ⚠ Timezone is UTC — diurnal peak may appear shifted in Kibana.")
        print("    Use --timezone 'America/New_York' to align with local business hours.\n")

    # Pre-flight: verify data streams exist
    print("  Checking data streams…")
    for ds in ["traces-apm-default", "logs-akamai.siem-mortgage", "logs-aws.waf-mortgage"]:
        try:
            es.indices.get_data_stream(name=ds)
            print(f"    ✓ {ds}")
        except Exception:
            print(f"    ✗ {ds} — NOT FOUND. Run bootstrap-prime-multilayer.py first.")
    print()
    print("  Press Ctrl+C to stop.\n")

    progress_q    = Queue()
    start_time    = time.time()
    apm_ok = apm_err = edge_ok = edge_err = 0
    _last_print   = [time.time()]

    def printer():
        nonlocal apm_ok, apm_err, edge_ok, edge_err
        while True:
            try:
                item = progress_q.get(timeout=1)
                if item is None:
                    break
                stream, result = item
                ok = result == "OK"
                if stream == "APM":
                    if ok: apm_ok  += 1
                    else:  apm_err += 1
                else:
                    if ok: edge_ok += 1
                    else:  edge_err += 1

                if not ok:
                    errs = apm_err + edge_err
                    if errs <= 50:
                        print(f"  ⚠ [{stream}] {result[4:]}")
                    elif errs == 51:
                        print("  (further errors suppressed)")

                now = time.time()
                if now - _last_print[0] >= 10:
                    elapsed = now - start_time
                    total   = apm_ok + edge_ok
                    rate    = total / elapsed if elapsed > 0 else 0
                    eta     = (total_docs - total) / rate if rate > 0 else 0
                    pct     = min(total / total_docs * 100, 100) if total_docs else 0
                    errs    = apm_err + edge_err
                    print(f"  [{pct:5.1f}%] {total:>10,}/~{total_docs:,}"
                          f"  |  {rate:>8,.0f} docs/sec"
                          f"  |  ETA {int(eta//3600):02d}h{int((eta%3600)//60):02d}m"
                          + (f"  |  {errs} errs" if errs else "")
                          + f"  [APM {apm_ok:,}  EDGE {edge_ok:,}]")
                    _last_print[0] = now
            except Empty:
                continue

    t_print = threading.Thread(target=printer, daemon=True)
    t_print.start()

    # ── APM and EDGE work queues (run concurrently) ───────────────────────────
    apm_q  = Queue()
    edge_q = Queue()
    for s in schedule:
        apm_q.put((s["day"], s["apm_count"]))
        edge_q.put((s["day"], s["edge_count"]))

    apm_workers_n  = max(1, workers // 2)
    edge_workers_n = max(1, workers - apm_workers_n)

    def _apm_worker():
        while True:
            try:
                day_dt, count = apm_q.get_nowait()
            except Empty:
                return
            try:
                _apm_day_worker(es, day_dt, count, anomaly_chance,
                                bulk_size_apm, pb_threads, pb_queue, progress_q, tz)
            except Exception as e:
                progress_q.put(("APM", f"ERR:{e}"))
            finally:
                apm_q.task_done()

    def _edge_worker():
        while True:
            try:
                day_dt, count = edge_q.get_nowait()
            except Empty:
                return
            try:
                _edge_day_worker(es, day_dt, count, bot_attack_chance,
                                 bulk_size_edge, pb_threads, pb_queue, progress_q, tz)
            except Exception as e:
                progress_q.put(("EDGE", f"ERR:{e}"))
            finally:
                edge_q.task_done()

    threads = (
        [threading.Thread(target=_apm_worker, daemon=True) for _ in range(min(apm_workers_n, days))]
        + [threading.Thread(target=_edge_worker, daemon=True) for _ in range(min(edge_workers_n, days))]
    )
    for t in threads:
        t.start()
    try:
        for t in threads:
            t.join()
    except KeyboardInterrupt:
        print("\n\nBackfill interrupted.")

    progress_q.put(None)
    t_print.join(timeout=10)

    elapsed = time.time() - start_time
    h, m, s = int(elapsed//3600), int((elapsed%3600)//60), int(elapsed%60)
    total_indexed = apm_ok + edge_ok
    rate          = total_indexed / elapsed if elapsed > 0 else 0
    errs          = apm_err + edge_err

    print(f"\n{'='*70}")
    print(f"  Backfill complete  —  {h:02d}h {m:02d}m {s:02d}s")
    print(f"  {'─'*66}")
    print(f"  APM  docs: {apm_ok:>12,}" + (f"  [{apm_err} errors]"  if apm_err  else ""))
    print(f"  EDGE docs: {edge_ok:>12,}" + (f"  [{edge_err} errors]" if edge_err else ""))
    print(f"  Total:     {total_indexed:>12,}  |  {rate:,.0f} docs/sec")
    if errs:
        print(f"  Errors:    {errs}")
    print()
    print(f"  Signals embedded:")
    print(f"    APM geo-anomalous traces: ~{anomaly_chance:.0%} "
          f"(~{round(total_apm_traces * anomaly_chance):,} of {total_apm_traces:,} traces)")
    print(f"    Edge bot/attack sessions: ~{bot_attack_chance:.0%} "
          f"(~{round(total_edge_events * bot_attack_chance):,} of {total_edge_events:,} events)")
    print(f"{'='*70}")

    # today excluded — return 0 so live mode generates a full day quota
    return days, apm_tpd, edge_epd, 0, 0


# ══════════════════════════════════════════════════════════════════════════════
# Live generation
# ══════════════════════════════════════════════════════════════════════════════

def run_live(es, apm_tpd, edge_epd, anomaly_chance, bot_attack_chance,
             backfill_days,
             backfill_today_apm, backfill_today_edge,
             bulk_size_apm, bulk_size_edge, pb_threads, pb_queue, tz):
    """
    Continuous live generation for all three streams.
    Generates today's remaining quota at the natural diurnal rate, then
    continues generating full days as midnight rolls.
    """
    tz_name = tz_str(tz)
    print(f"\n{'='*70}")
    print(f"  Live generation — {tz_name}")
    print(f"  APM:  ~{apm_tpd:,} traces/weekday  |  anomaly: {anomaly_chance:.0%}")
    print(f"  Edge: ~{edge_epd:,} events/weekday  |  bot/attack: {bot_attack_chance:.0%}")
    print(f"  Backfill covered: {backfill_days} days")
    print(f"  Press Ctrl+C to stop.\n")

    metrics_counter = [0]
    indexed_live    = 0
    error_count     = 0
    start_time      = time.time()
    _last_status    = time.time()
    current_day     = datetime.now(tz).date()

    apm_target  = max(0, round(apm_tpd  * _day_factor(current_day)) - backfill_today_apm)
    edge_target = max(0, round(edge_epd * _day_factor(current_day)) - backfill_today_edge)

    end_of_today = (
        datetime(current_day.year, current_day.month, current_day.day, tzinfo=tz)
        + timedelta(days=1)
    ).astimezone(timezone.utc)

    print(f"  Today ({current_day}):")
    print(f"    APM  backfill wrote: {backfill_today_apm:>6,}  remaining: {apm_target:>6,}")
    print(f"    EDGE backfill wrote: {backfill_today_edge:>6,}  remaining: {edge_target:>6,}")
    print(f"    Day rolls at: {end_of_today.strftime('%H:%M:%S UTC')}\n")

    def _status():
        nonlocal _last_status
        if time.time() - _last_status >= 30:
            print(f"  [{indexed_live:,} live docs"
                  + (f", {error_count} errors" if error_count else "") + "]")
            _last_status = time.time()

    def _index_batch(actions):
        nonlocal indexed_live, error_count
        try:
            for ok, info in parallel_bulk(
                es, actions,
                thread_count=1, chunk_size=len(actions),
                queue_size=1, raise_on_error=False,
                raise_on_exception=False, request_timeout=30,
            ):
                if ok:
                    indexed_live += 1
                else:
                    error_count += 1
        except Exception:
            error_count += 1

    # ── Merge APM + EDGE event streams for today, sorted by timestamp ─────────
    # We interleave both streams so that correlated events (same session IP)
    # arrive in timestamp order — more realistic for streaming ingest.

    apm_events  = list(timestamps_from_now(apm_target,  tz))  # [(ts, sleep_s), ...]
    edge_events = list(timestamps_from_now(edge_target, tz))

    # Merge and sort by timestamp; tag each event with its stream
    combined = (
        [(ts, s, "APM")  for ts, s in apm_events] +
        [(ts, s, "EDGE") for ts, s in edge_events]
    )
    combined.sort(key=lambda x: x[0])

    prev_time = time.time()
    for ts_str, sleep_s, stream in combined:
        now = time.time()
        actual_sleep = max(0.0, sleep_s - (now - prev_time))
        if actual_sleep > 0:
            time.sleep(min(actual_sleep, 5.0))
        prev_time = time.time()

        if stream == "APM":
            actions = _make_apm_actions(ts_str, anomaly_chance, metrics_counter)
        else:
            is_attack = random.random() < bot_attack_chance
            actions   = _make_edge_actions(ts_str, is_attack)

        _index_batch(actions)
        _status()

    # ── Full days after midnight ───────────────────────────────────────────────
    print(f"\n  Today's quota complete — entering continuous daily mode.")
    print(f"  Ctrl+C to stop.\n")

    while True:
        now_local   = datetime.now(tz)
        today       = now_local.date()
        apm_count   = max(1, round(apm_tpd  * _day_factor(today)))
        edge_count  = max(1, round(edge_epd * _day_factor(today)))
        midnight    = (datetime(today.year, today.month, today.day, tzinfo=tz)
                       + timedelta(days=1)).astimezone(timezone.utc)
        secs_left   = max(1, (midnight - datetime.now(timezone.utc)).total_seconds())
        total_day   = apm_count + edge_count
        sleep_per   = secs_left / total_day

        try:
            while datetime.now(tz).date() == today:
                ts_str = _fmt_ts(datetime.now(timezone.utc))
                # Alternate APM and EDGE events at a 1:4 ratio (1 trace ≈ 10 docs,
                # 1 edge pair = 2 docs; keeping the ratio ~equal in doc count)
                stream  = "APM" if random.random() < (apm_count / (apm_count + edge_count)) else "EDGE"
                if stream == "APM":
                    actions = _make_apm_actions(ts_str, anomaly_chance, metrics_counter)
                else:
                    is_attack = random.random() < bot_attack_chance
                    actions   = _make_edge_actions(ts_str, is_attack)
                _index_batch(actions)
                time.sleep(max(0.001, sleep_per))
                _status()
        except KeyboardInterrupt:
            break

    elapsed = time.time() - start_time
    h, m, s = int(elapsed//3600), int((elapsed%3600)//60), int(elapsed%60)
    print(f"\n{'='*70}")
    print(f"  Live generation stopped after {h:02d}h {m:02d}m {s:02d}s")
    print(f"  Live docs indexed: {indexed_live:,}"
          + (f"  |  {error_count} errors" if error_count else ""))
    print(f"{'='*70}\n")


