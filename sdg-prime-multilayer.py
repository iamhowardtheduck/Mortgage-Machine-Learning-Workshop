    p.add_argument("--anomaly-chance",         type=float, default=DEFAULT_ANOMALY,
                   metavar="RATE",
                   help=f"Fraction of APM traces with geo-anomalous IPs (default: {DEFAULT_ANOMALY}). "
                        f"Raise to 0.15–0.30 for stronger outlier signals.")
    p.add_argument("--bot-attack-chance",      type=float, default=DEFAULT_BOT_ATTACK,
                   metavar="RATE",
                   help=f"Fraction of edge events that are bot/attack sessions (default: {DEFAULT_BOT_ATTACK}). "
                        f"Attack sessions have high bot scores, elevated risk, and BLOCK actions.")

