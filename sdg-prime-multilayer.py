def _random_ip(geo):
    """
    Return a plausible random IP in a /8 subnet roughly associated with the
    given country code.  Not geo-accurate — just distinctive enough that the
    ML model sees a consistent per-country distribution.
    """
    # Use a deterministic prefix per country so the same country always maps to
    # the same subnet family.  This makes the geo signal more learnable.
    _prefix = {
        "US": (17,  40),   "NG": (197, 212),
        "RU": (178, 185),  "CN": (36,  123),
        "RO": (5,   79),   "BY": (178, 193),
        "IR": (5,   185),  "KP": (175, 175),
    }
    lo, hi = _prefix.get(geo.get("country", "US"), (10, 200))
    a = random.randint(lo, hi)
    b, c, d = random.randint(0, 255), random.randint(0, 255), random.randint(1, 254)
    return f"{a}.{b}.{c}.{d}"


