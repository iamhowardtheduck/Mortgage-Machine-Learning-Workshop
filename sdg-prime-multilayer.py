def _make_edge_actions(ts_iso, is_attack):
    """
    Generate one correlated pair: one Akamai SIEM event + one AWS WAF event.
    Both share the same source IP so the DFA multi-index job finds them.
    Returns a list of two bulk action dicts.
    """
    geo       = random.choice(_ANOMALOUS_GEOS) if is_attack else random.choice(_NORMAL_GEOS)
    akamai    = _akamai_doc(ts_iso, is_attack, geo)
    akamai_ip = akamai["_source"]["source"]["ip"]
    waf       = _waf_doc(ts_iso, is_attack, geo, akamai_ip)
    return [akamai, waf]


