    for name, val in [("--anomaly-chance", args.anomaly_chance),
                      ("--bot-attack-chance", args.bot_attack_chance)]:
        if not (0.0 <= val <= 1.0):
            print(f"ERROR: {name} must be between 0.0 and 1.0 (got {val})")
            sys.exit(1)

